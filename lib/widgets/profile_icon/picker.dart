class ProfileIconPicker extends StatefulWidget {
  final String initialValue;
  final List<String> icons;
  final Function(String) onIconChanged;

  const ProfileIconPicker({
    Key? key,
    this.initialValue = '👻',
    this.icons = defaultIcons,
    required this.onIconChanged,
  }) : super(key: key);

  @override
  ProfileIconPickerState createState() => ProfileIconPickerState();
}

class ProfileIconPickerState extends State<ProfileIconPicker> {
  late String _icon;

  @override
  void initState() {
    super.initState();

    _icon = widget.initialValue;
  }

  void onIconSelected(String icon) {
    HapticFeedback.heavyImpact();
    setState(() {
      _icon = icon;
    });

    widget.onIconChanged(icon);
  }

  @override
  Widget build(BuildContext context) {
    return SizedBox(
      height: 162,
      child: CustomScrollView(
        scrollDirection: Axis.horizontal,
        slivers: [
          SliverGrid(
            delegate:
                SliverChildBuilderDelegate(childCount: widget.icons.length,
                    (BuildContext context, int index) {
              final icon = widget.icons[index];
              return GestureDetector(
                onTap: () => onIconSelected(icon),
                child: ProfileIcon(
                  icon,
                  border: _icon == icon
                      ? Border.all(width: 1, color: CupertinoColors.activeBlue)
                      : null,
                ),
              );
            }),
            gridDelegate: const SliverGridDelegateWithMaxCrossAxisExtent(
              maxCrossAxisExtent: 40.0,
              mainAxisSpacing: 10.0,
              crossAxisSpacing: 10.0,
              childAspectRatio: 1.0,
            ),
          ),
        ],
      ),
    );
  }
}
